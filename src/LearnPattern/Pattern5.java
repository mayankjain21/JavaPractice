package LearnPattern;

public class Pattern5 {
	
	public static void main(String[] args)
	{
		int nst=1;
		 for(int i=1;i<=5;i++)
		 {
			 
			 for(int j=1;j<=nst;j++)
			 {
				 System.out.print(" *");
			 }
			 System.out.println();
			 nst++;
		 }
		 int nstr=4;
		 for(int i=1;i<=5;i++)
		 {
			 
			 for(int j=1;j<=nstr;j++)
			 {
				 System.out.print(" *");
			 }
			 System.out.println();
			 nstr--;
		 }
		 
		 
		 
		 
		 
		 
	}

}
